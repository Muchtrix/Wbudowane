#define BASE_DELAY 4
void display_char(char c){
    for(int i = 0; i < 3; ++i){
        PORTB = letters[c][i];
        _delay_ms(BASE_DELAY);
    }
    PORTB = 0x00;
    _delay_ms(BASE_DELAY);
}

